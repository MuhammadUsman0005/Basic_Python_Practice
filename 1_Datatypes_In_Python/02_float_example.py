# Concept: float (Floating Point Numbers)   * float is a keyword it's a reserved word.
# Float are numbers that have decimal points. Float is a datatype.
# Example: 2.4, 2.6, 6.3, 3.14 etc

'''
Real-life Example:
Suppose you're buying something online. 
You have to calculate the total cost including tax.
'''

price = 99.9

tax = 5.50 # Tax fee on product price (99.9)

total = price + tax
# final amount
print("Total amount to pay is: ",total)