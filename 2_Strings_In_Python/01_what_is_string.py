# Concept: String
# A string is a sequence of characters enclosed in quotes.

# We can use single quotes:
name1 = 'Usman'
# Or double quotes:
name2 = "Muhammad"
# Or triple quotes for multiline strings:
about_me = '''I'm learning Python. Let's learn together!
step by step in easy way.'''

# check the type of variable
print("Type of name1: ",type(name1))          # <class 'str'>
print("Type of about_me: ",type(about_me))          # <class 'str'>